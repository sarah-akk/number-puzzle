#include "Window.h"
#include <iostream>


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Window::updateViewportGLFW(GLFWwindow* window, int width, int height)
{
	glViewport(0, 0, width, height);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void   Window::poll()
{
	glfwPollEvents();
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int  Window::shouldClose(GLFWwindow* window)
{
	return glfwWindowShouldClose(window);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

WindowSize Window::getWindowSize(GLFWwindow* window)
{
	WindowSize info(0, 0);
	glfwGetFramebufferSize(window, &info.width, &info.height);

	return info;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Window::clear(GLFWwindow* window)
{
	glfwSwapBuffers(window);
}

